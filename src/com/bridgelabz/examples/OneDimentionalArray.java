package com.bridgelabz.examples;

public class OneDimentionalArray {

	public static void main(String[] args) {
		int[] arr = new int[3];
		
		arr[0] = 10;
		arr[1] = 20;
		arr[2] = 30;
		
		System.out.println("Element of index 0: " + arr[0]);
		System.out.println("Element of index 1: " + arr[1]);
		System.out.println("Element of index 2: " + arr[2]);
	}
}